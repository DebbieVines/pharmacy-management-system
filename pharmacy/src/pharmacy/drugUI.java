    private void deleteButtActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_deleteButtActionPerformed
        DefaultTableModel model = (DefaultTableModel) jTable.getModel();
        if(jTable.getSelectedRow()==-1){
            if(jTable.getRowCount()==0){
                JOptionPane.showMessageDialog(null,"no data to delete","pharmacy managmnet system",JOptionPane.OK_OPTION);
            }
            else
                JOptionPane.showMessageDialog(null,"select a row to delete","pharmacy managmnet system",JOptionPane.OK_OPTION);
        }
        else{
            drugLinkedList list = new drugLinkedList();
            list.deleteAt(jTable.getSelectedRow());
            model.removeRow(jTable.getSelectedRow());
            
        }
    }//GEN-LAST:event_deleteButtActionPerformed


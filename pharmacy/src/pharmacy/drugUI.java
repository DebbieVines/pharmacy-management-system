    private void addButtActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_addButtActionPerformed
        DefaultTableModel model = (DefaultTableModel) jTable.getModel();
        
        if(nameField.getText().equals("")||priceField.getText().equals("")||ammountField.getText().equals("")||expField.getText().equals("")
                 ||companyField.getText().equals("")||sideEffectField.getText().equals("")||howToField.getText().equals("")||infoField.getText().equals("")){
            errorLabel.setText("Error: every field must be field approperietly");
        }
       else{
            String name = nameField.getText();
            float price = Float.parseFloat(priceField.getText());
            int ammount = Integer.parseInt(ammountField.getText());
            String exp = expField.getText();
            String company = companyField.getText();
            String sideEffect = sideEffectField.getText();
            String howTo = howToField.getText();
            String info = infoField.getText();
            
            drug dr = new drug(name,price,ammount,exp,company,sideEffect,howTo,info);
            dr.addDrugs(dr);
            model.addRow(new Object[]{name,price,ammount,exp,company,sideEffect,howTo,info});
        }
    }//GEN-LAST:event_addButtActionPerformed

